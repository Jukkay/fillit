#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t			i;
	char			*str;

	i = 0;
	if (!s1 || !s2)
		return (NULL);
	str = ft_strnew(ft_strlen((char *)s1) + ft_strlen((char *)s2));
	if (!str)
		return (NULL);
	while (*s1)
	{
		str[i] = *s1;
		i++;
		s1++;
	}
	while (*s2)
	{
		str[i] = *s2;
		i++;
		s2++;
	}
	return (str);
}
